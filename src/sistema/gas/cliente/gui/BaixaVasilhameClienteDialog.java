    private void jB_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_addActionPerformed
        BigDecimal qtd_pendente = new BigDecimal(jTF_qtd_pendente.getText());
        BigDecimal qtd_restante = new BigDecimal(jTF_qtd_restante.getText());
        BigDecimal qtd_devolver = new BigDecimal(jTF_qtd_a_devolver.getText());
        BigDecimal qtd_devolvida = new BigDecimal(jTF_qtd_devolvida.getText());
        if ((qtd_devolver.compareTo(qtd_restante) <= 0) & !(qtd_devolver.compareTo(BigDecimal.ZERO) < 0) & !(qtd_devolver.compareTo(BigDecimal.ZERO) == 0)) {
            DefaultTableModel dtm = (DefaultTableModel) jT_detalhe.getModel();
            dtm.addRow(new Object[]{
                fmtBrasilDataHora.format(new Date()),
                qtd_devolver,
                false
            });
            qtd_devolvida = qtd_devolvida.add(qtd_devolver);
            qtd_restante = qtd_pendente.subtract(qtd_devolvida);
            jTF_qtd_devolvida.setText(qtd_devolvida.setScale(2, RoundingMode.HALF_DOWN).toString());
            jTF_qtd_restante.setText(qtd_restante.setScale(2, RoundingMode.HALF_DOWN).toString());

            if (qtd_restante.compareTo(BigDecimal.ZERO) == 0) {
                jB_add.setEnabled(false);
                clienteVasilhame.setStatus("Q");
            }
            jTF_qtd_a_devolver.setText("0.00");
        } else if (qtd_devolver.compareTo(BigDecimal.ZERO) < 0) {
            JOptionPane.showMessageDialog(rootPane, "Números negativos não são válidos aqui!", "ALERTA DO SISTEMA", JOptionPane.WARNING_MESSAGE);
        } else if (qtd_devolver.compareTo(BigDecimal.ZERO) == 0) {
            JOptionPane.showMessageDialog(rootPane, "Zero não é um número válido aqui!", "ALERTA DO SISTEMA", JOptionPane.WARNING_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Quantidade superior a quantidade restante!", "ALERTA DO SISTEMA", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jB_addActionPerformed

