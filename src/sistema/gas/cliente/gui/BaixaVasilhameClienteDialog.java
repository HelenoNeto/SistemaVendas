    public BaixaVasilhameClienteDialog(java.awt.Frame parent, boolean modal, ClienteVasilhame clienteVasilhame) {
        super(parent, modal);
        try {
            initComponents();

            this.clienteVasilhame = clienteVasilhame;

            DefaultTableModel dtm = (DefaultTableModel) jT_detalhe.getModel();
            ArrayList<ClienteVasilhameDetalhe> listaClienteVasilhameDetalhe = ConectaBDClienteVasilhameDetalhe.getInstance().consultarClienteVasilhameDetalhe("select * from cliente_vasilhame_detalhe where id_cliente_vasilhame = " + clienteVasilhame.getId());
            for (ClienteVasilhameDetalhe clienteVasilhameDetalhe : listaClienteVasilhameDetalhe) {
                dtm.addRow(new Object[]{
                    fmtBrasilDataHora.format(clienteVasilhameDetalhe.getData_hora_transacao()),
                    clienteVasilhameDetalhe.getQuantidade_devolvida(),
                    true
                });
            }

            vendaDetalhe = Fachada.getInstancia().consultarVendaDetalhe_porIdVendaDetalhe(clienteVasilhame.getId_venda_detalhe());
            jTF_cod_produto.setText(vendaDetalhe.getProduto().getCodigo());
            jTF_desc_produto.setText(vendaDetalhe.getProduto().getDescricao());
            prodVasilhame = ConectaBDProduto.getInstance().consultarProduto_porId(clienteVasilhame.getId_produto_vasilhame());
            jTF_cod_produto_vasilhame.setText(prodVasilhame.getCodigo());
            jTF_desc_produto_vasilhame.setText(prodVasilhame.getDescricao());
            jTF_qtd_pendente.setText(clienteVasilhame.getQuantidade().setScale(2, RoundingMode.HALF_DOWN).toString());
            BigDecimal qtdTotalDevolvida = ConectaBDClienteVasilhameDetalhe.getInstance().consultarTotalDevolvidoClienteVasilhameDetalhe(clienteVasilhame.getId());
            jTF_qtd_devolvida.setText(qtdTotalDevolvida.setScale(2, RoundingMode.HALF_DOWN).toString());
            jTF_qtd_restante.setText(clienteVasilhame.getQuantidade().subtract(qtdTotalDevolvida).setScale(2, RoundingMode.HALF_DOWN).toString());
            jTF_qtd_a_devolver.setText("0.00");

            jB_add.setEnabled(!(clienteVasilhame.getQuantidade().subtract(qtdTotalDevolvida).setScale(2, RoundingMode.HALF_DOWN).compareTo(BigDecimal.ZERO) == 0));
            jB_confirmar.setEnabled(!(clienteVasilhame.getQuantidade().subtract(qtdTotalDevolvida).setScale(2, RoundingMode.HALF_DOWN).compareTo(BigDecimal.ZERO) == 0));

        } catch (SQLException ex) {
            Logger.getLogger(BaixaVasilhameClienteDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

