    private void jT_detalheKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jT_detalheKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_DELETE) {
            if (JOptionPane.showConfirmDialog(rootPane, "Deseja excluir a devolução?", "PERGUNTA DO SISTEMA", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {
                DefaultTableModel dtm = (DefaultTableModel) jT_detalhe.getModel();
                BigDecimal qtd_devolvida_detalhe = (BigDecimal) jT_detalhe.getModel().getValueAt(jT_detalhe.getSelectedRow(), 1);
                BigDecimal qtd_restante = new BigDecimal(jTF_qtd_restante.getText());
                BigDecimal qtd_devolvida = new BigDecimal(jTF_qtd_devolvida.getText());
                dtm.removeRow(jT_detalhe.getSelectedRow());
                qtd_devolvida = qtd_devolvida.subtract(qtd_devolvida_detalhe);
                qtd_restante = qtd_restante.add(qtd_devolvida_detalhe);
                jTF_qtd_devolvida.setText(qtd_devolvida.setScale(2, RoundingMode.HALF_DOWN).toString());
                jTF_qtd_restante.setText(qtd_restante.setScale(2, RoundingMode.HALF_DOWN).toString());
            }
        }
    }//GEN-LAST:event_jT_detalheKeyPressed

    private void jB_confirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_confirmarActionPerformed
        try {
            for (ClienteVasilhameDetalhe clienteVasilhameDetalhe : clienteVasilhame.getListaClienteVasilhameDetalhe()) {
                ConectaBDClienteVasilhameDetalhe.getInstance().excluirClienteVasilhameDetalheUnico(clienteVasilhameDetalhe);
            }

            for (int i = 0; i < jT_detalhe.getRowCount(); i++) {
                ClienteVasilhameDetalhe clienteVasilhameDetalhe = new ClienteVasilhameDetalhe();
                String dataHoraTabela = (String) jT_detalhe.getModel().getValueAt(i, 0);
                BigDecimal qtd_devolvida = (BigDecimal) jT_detalhe.getModel().getValueAt(i, 1);
                boolean banco = (Boolean) jT_detalhe.getModel().getValueAt(i, 2);
                Date datahora = fmtBrasilDataHora.parse(dataHoraTabela);
                clienteVasilhameDetalhe.setData_hora_transacao(datahora);
                clienteVasilhameDetalhe.setId_cliente_vasilhame(clienteVasilhame.getId());
                clienteVasilhameDetalhe.setQuantidade_devolvida(qtd_devolvida);
                ConectaBDClienteVasilhameDetalhe.getInstance().incluirClienteVasilhameDetalhe(clienteVasilhameDetalhe);
                if (!banco) {
                    ConectaBDProduto.getInstance().adicionarEstoqueProduto(qtd_devolvida, prodVasilhame);
                }
            }
            ConectaBDClienteVasilhame.getInstance().editaClienteVasilhame(clienteVasilhame);
            JOptionPane.showMessageDialog(rootPane, "salvo com sucesso!", "ALERTA DO SISTEMA", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        } catch (SQLException | ParseException ex) {
            Logger.getLogger(BaixaVasilhameClienteDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jB_confirmarActionPerformed

