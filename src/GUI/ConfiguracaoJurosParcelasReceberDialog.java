    private void jB_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_salvarActionPerformed
        try {
            boolean salvar;
            if (configuracaoJurosParcelasReceber == null) {
                configuracaoJurosParcelasReceber = new ConfiguracaoJurosParcelasReceber();
                salvar = true;
            } else {
                salvar = false;
            }
            jRB_aa.setActionCommand("AA");
            jRB_ad.setActionCommand("AD");
            jRB_am.setActionCommand("AM");
            configuracaoJurosParcelasReceber.setTipo_juros(buttonGroup_opcao.getSelection().getActionCommand());
            BigDecimal juros = new BigDecimal(jTF_valor.getText());
            BigDecimal cem = new BigDecimal(100);
            configuracaoJurosParcelasReceber.setValor_juros(juros.divide(cem).setScale(6, RoundingMode.HALF_DOWN));
            if (salvar) {
                conexao.incluirConfiguracaoJurosParcelasReceber(configuracaoJurosParcelasReceber);
                JOptionPane.showMessageDialog(rootPane, "Salvo com sucesso!", "Alerta do Sistema", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            } else {
                conexao.editarConfiguracaoJurosParcelasReceber(configuracaoJurosParcelasReceber);
                JOptionPane.showMessageDialog(rootPane, "Editado com sucesso!", "Alerta do Sistema", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao realizar operaÃ§Ã£o!", "Alerta do Sistema", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jB_salvarActionPerformed

