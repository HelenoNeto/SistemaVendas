    private void jTF_lucroFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTF_lucroFocusLost
        FocusLostFormaterDecimal(evt);
        BigDecimal valorCompra = ClasseUtilitaria.parseToBig(jTF_valor_compra.getText().isEmpty() ? "0" : jTF_valor_compra.getText());
        BigDecimal pLucro = ClasseUtilitaria.parseToBig(jTF_lucro.getText());
        BigDecimal vLucro = valorCompra.multiply(pLucro.divide(ClasseUtilitaria.parseToBig("100"), 6, RoundingMode.HALF_DOWN));
        if (valorCompra.compareTo(BigDecimal.ZERO) != 0) {
            jTF_valor_venda.setText(ClasseUtilitaria.fmtBig(valorCompra.add(vLucro), 2));
            jTF_lucro_real.setText(ClasseUtilitaria.fmtBig(vLucro, 2));
        }
    }//GEN-LAST:event_jTF_lucroFocusLost

