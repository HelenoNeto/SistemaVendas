    private void calculaValorVenda(String lucro) {
        BigDecimal valorCompra = ClasseUtilitaria.parseToBig(jTF_valor_compra.getText().isEmpty() ? "0" : jTF_valor_compra.getText());
        BigDecimal valorLucroPorcentagem = ClasseUtilitaria.parseToBig(lucro.isEmpty() ? "0" : lucro);
        if (valorLucroPorcentagem.compareTo(BigDecimal.ZERO) != 0) {
            BigDecimal valorVenda = valorCompra.add(valorCompra.multiply(valorLucroPorcentagem.divide(ClasseUtilitaria.parseToBig("100"))));
            BigDecimal lucroReal = valorVenda.subtract(valorCompra);
            jTF_valor_venda.setText(valorVenda.setScale(2, RoundingMode.HALF_DOWN).toString());
            jTF_lucro_real.setText(lucroReal.setScale(2, RoundingMode.HALF_DOWN).toString());
        }
    }

    private void calculaValorVendaInverso() {
        BigDecimal valorCompra = ClasseUtilitaria.parseToBig(jTF_valor_compra.getText().isEmpty() ? "0" : jTF_valor_compra.getText());
        BigDecimal valorVenda = ClasseUtilitaria.parseToBig(jTF_valor_venda.getText().isEmpty() ? "0" : jTF_valor_venda.getText());
        if (valorCompra.compareTo(BigDecimal.ZERO) != 0) {
            BigDecimal valorLucroPorcentagem = ((valorVenda.subtract(valorCompra)).divide(valorCompra, 4, RoundingMode.HALF_DOWN)).multiply(ClasseUtilitaria.parseToBig("100"));
            BigDecimal valorLucroReal = (valorVenda.subtract(valorCompra));
            jTF_lucro.setText(ClasseUtilitaria.fmtBig(valorLucroPorcentagem, 4));
            jTF_lucro_real.setText(ClasseUtilitaria.fmtBig(valorLucroReal, 2));
        }
    }

