    private boolean contemCamposObrigatorios() {
        boolean temCampoVazio = false;
        JTextField campos[] = {jTF_codigo, jTF_descricao, jTF_cod_grupo, jTF_desc_grupo, jTF_qtd_estoque, jTF_qtd_estoque_avaria, jTF_valor_venda, jTF_valor_compra, jTF_lucro};
        for (JTextField jTextField : campos) {
            if (jTextField.getText().isEmpty()) {
                jTextField.setBackground(Color.yellow);
                temCampoVazio = true;
            } else {
                jTextField.setBackground(Color.white);
            }
        }

        if (jcbUnidade.getSelectedItem() == null
                || ((UnidadeProduto) jcbUnidade.getSelectedItem()).getId() == 0) {
            temCampoVazio = true;
        }

        if (temCampoVazio) {
            JOptionPane.showMessageDialog(null, "CAMPO(s) OBRIGATÓRIO(s) EM BRANCO!", null, 2);
            return true;
        } else {
            return false;
        }
    }

