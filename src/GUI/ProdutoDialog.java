    private void actionPesquisar() {
        jTF_pesquisar.addKeyListener(new KeyAdapter() {

            @Override
            public void keyPressed(KeyEvent e) {
                if (e.getKeyCode() == KeyEvent.VK_ENTER) {
                    try {
                        pesquisar();
                    } catch (SQLException ex) {
                        Logger.getLogger(ListaProdutoVasilhameDialog.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else if (e.getKeyCode() == KeyEvent.VK_DOWN) {
                    if (jT_consulta.getRowCount() > 0) {
                        jT_consulta.addRowSelectionInterval(0, 0);
                        jT_consulta.requestFocus();
                    }
                }
            }
        });
    }

