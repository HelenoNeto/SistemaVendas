    private void jTF_cod_grupoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTF_cod_grupoKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER && (!jTF_cod_grupo.getText().isEmpty())) {
            try {
                ArrayList<Grupo> listaGrupo = Fachada.getInstancia().consultarGrupo("select * from grupo where id = " + jTF_cod_grupo.getText().trim());
                if (!listaGrupo.isEmpty()) {
                    grupo = listaGrupo.get(0);
//                    liberarCamposVasilhames(grupo.isVasilhames());
                    jTF_cod_grupo.setText(String.valueOf(grupo.getId()));
                    jTF_desc_grupo.setText(grupo.getDescricao());

                    jTF_qtd_estoque.requestFocus();
                }
            } catch (SQLException ex) {
                Logger.getLogger(ProdutoDialog.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (evt.getKeyCode() == KeyEvent.VK_F1) {
            GrupoDialogGUI grupoDialogGUI = new GrupoDialogGUI(null, true, true);
            grupoDialogGUI.setLocationRelativeTo(null);
            grupoDialogGUI.setVisible(true);
            grupo = grupoDialogGUI.getGrupo();
            if (grupo != null) {
//                liberarCamposVasilhames(grupo.isVasilhames());
                jTF_cod_grupo.setText(String.valueOf(grupo.getId()));
                jTF_desc_grupo.setText(grupo.getDescricao());

                jTF_qtd_estoque.requestFocus();
            }
        }
    }//GEN-LAST:event_jTF_cod_grupoKeyPressed

