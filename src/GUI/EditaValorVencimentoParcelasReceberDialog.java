    public ArrayList<FinanceiroParcelas> importaListaParcelas() {
        if (cancelou) {
            return null;
        } else {
            return (ArrayList<FinanceiroParcelas>) model.getDataSet();
        }
    }

    public double importaEntrada() {
        if (cancelou) {
            return 0;
        } else {
            return Double.parseDouble(jTF_entrada.getText());
        }
    }

    private void configurarTableModel() {
        jtParcelas.setModel(new TableModelEditarValorFinanceiro(jtParcelas));
        jtParcelas.setSelectionModel(new DefaultListSelectionModel() {

            @Override
            public String toString() {
                return "gridEditarValorFinanceiro";
            }
        });

        jtParcelas.setAutoCreateColumnsFromModel(false);
        jtParcelas.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        FontMetrics fm = jtParcelas.getFontMetrics(jtParcelas.getFont());
        jtParcelas.setColumnModel(new EditarValorFinanceiroColumnModel(fm));
        jtParcelas.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);

        model = (TableModelEditarValorFinanceiro) jtParcelas.getModel();
    }
