    private void jtParcelasKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtParcelasKeyReleased
        BigDecimal decimalTotalParcelas = ClasseUtilitaria.parseToBig(jTF_total.getText());
        BigDecimal decimalEntrada = ClasseUtilitaria.parseToBig(jTF_entrada.getText());
        jTF_restante.setText(ClasseUtilitaria.fmtBig(decimalTotalParcelas.subtract(model.getTotalParcelas().add(decimalEntrada)), 2));

        if (ClasseUtilitaria.parseToBig(jTF_restante.getText()).compareTo(BigDecimal.ZERO) == 0 && !contemParcelaPaga) {
            jB_confirmar.setEnabled(true);
        } else {
            jB_confirmar.setEnabled(false);
        }
    }//GEN-LAST:event_jtParcelasKeyReleased

